import math

print(math.sqrt(25))
print(math.ceil(4.3))
print(math.lcm(9, 12))
print(math.gcd(20, 2))
print(math.fmod(21, 2))
print(math.pow(2, 3))

# Additional math module functions
print(math.floor(4.7))         # Floor value
print(math.factorial(5))       # Factorial
print(math.exp(2))             # Exponential (e^2)
print(math.log(100, 10))       # Logarithm base 10
print(math.log2(8))            # Logarithm base 2
print(math.log10(1000))        # Logarithm base 10
print(math.sin(math.pi / 2))   # Sine
print(math.cos(0))             # Cosine
print(math.tan(math.pi / 4))   # Tangent
print(math.degrees(math.pi))   # Convert radians to degrees
print(math.radians(180))       # Convert degrees to radians
print(math.isqrt(16))          # Integer square root
print(math.copysign(2, -3))    # Copy sign
print(math.trunc(4.9))         # Truncate decimal
print(math.remainder(10, 3))   # IEEE 754-style remainder
print(math.hypot(3, 4))        # Euclidean norm
print(math.prod([1, 2, 3, 4])) # Product of iterable